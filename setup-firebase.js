#!/usr/bin/env node

const fs = require('fs');
const readline = require('readline');

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

console.log('🔥 Firebase Configuration Setup');
console.log('================================\n');

console.log('Please provide your Firebase configuration values:');
console.log('(You can find these in your Firebase Console > Project Settings > General > Your apps)\n');

const questions = [
  { key: 'NEXT_PUBLIC_FIREBASE_API_KEY', prompt: 'API Key: ' },
  { key: 'NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN', prompt: 'Auth Domain: ' },
  { key: 'NEXT_PUBLIC_FIREBASE_PROJECT_ID', prompt: 'Project ID: ' },
  { key: 'NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET', prompt: 'Storage Bucket: ' },
  { key: 'NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID', prompt: 'Messaging Sender ID: ' },
  { key: 'NEXT_PUBLIC_FIREBASE_APP_ID', prompt: 'App ID: ' }
];

const config = {};

async function askQuestion(question) {
  return new Promise((resolve) => {
    rl.question(question.prompt, (answer) => {
      config[question.key] = answer.trim();
      resolve();
    });
  });
}

async function setupConfig() {
  for (const question of questions) {
    await askQuestion(question);
  }

  // Generate .env.local content
  let envContent = '# Firebase Configuration\n';
  envContent += '# Generated by setup script\n\n';
  
  for (const [key, value] of Object.entries(config)) {
    envContent += `${key}=${value}\n`;
  }
  
  envContent += '\n# Optional: Enable development features\n';
  envContent += 'NEXT_PUBLIC_DEV_MODE=true\n';

  // Write to .env.local
  fs.writeFileSync('.env.local', envContent);
  
  console.log('\n✅ Configuration saved to .env.local');
  console.log('\n🚀 Next steps:');
  console.log('1. Run: npm run dev');
  console.log('2. Open: http://localhost:3012');
  console.log('3. Test Google sign-in');
  
  rl.close();
}

setupConfig().catch(console.error);